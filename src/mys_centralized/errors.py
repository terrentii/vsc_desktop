"""Типизированные ошибки централизованного модуля.

Сетевой слой не знает деталей хранилища/крипто; ошибки — единая иерархия для
контроллера/UI.
"""


class CentralizedError(Exception):
    """Базовая ошибка централизованного режима."""


class AuthError(CentralizedError):
    """Неверные учётные данные или истёкший/недействительный токен (HTTP 401)."""


class ServerError(CentralizedError):
    """Сервер ответил ошибкой уровня приложения (4xx, кроме 401)."""


class NetworkError(CentralizedError):
    """Сеть недоступна, таймаут, обрыв соединения или 5xx."""


class ProtocolError(CentralizedError):
    """Ответ сервера не соответствует ожидаемому контракту (битый JSON/форма)."""
